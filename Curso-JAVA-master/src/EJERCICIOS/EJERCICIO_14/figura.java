package EJERCICIOS.EJERCICIO_14;

public abstract class figura {
    private double area;
    private double perimetro;

    public figura(double area, double perimetro){
        this.area = this.area;
        this.perimetro = this.perimetro;
    }
    public double getArea() { return area; }
    public double getPerimetro() { return perimetro; }
}